
// 1. Example of Unary Operators:

let num = 5;
num++;  // Increment by one (unary plus operator)
let isTrue = true;
isTrue = !isTrue;  // Logical NOT (unary negation)

// 2. Example of Binary Operators:

let x = 5;
let y = 3;
let sum = x + y;  // Addition (binary plus operator)
let isEqual = x === y;  // Strict equality (binary equality operator)

// 3. Exmaple of Ternary Operator:

let age = 18;
let canVote = age >= 18 ? "Yes" : "No";



