// Q5. Use the while loop to print numbers from 1 to 25 in ascending and descending order.

let number1 = 1;
console.log("Ascending Order:");

while (number1 <= 25) {
    let ascendingNumber = number1++; 
    console.log(ascendingNumber);
}

let number2 = 25;
console.log("Descending Orde:");

while (number2 >= 1) {
    let descendingNumber = number2--; 
    console.log(descendingNumber); 
}