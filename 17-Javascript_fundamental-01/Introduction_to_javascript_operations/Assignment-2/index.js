
// 1. Arithmetic Operators

let sum = 5 + 3;       // Addition
let difference = 7 - 2; // Subtraction
let product = 4 * 6;    // Multiplication
let quotient = 8 / 2;   // Division
let remainder = 10 % 3; // Modulus (remainder)


// 2. Comparison Operators
let num1 = 5;
let num2 = 5;

let isEqual = num1 == num2;   // Equal to
let isNotEqual = num1 != num2; // Not equal to
let isGreaterThan = num1 > num2; // Greater than
let isLessThan = num1 < num2;    // Less than
let isTrue = true === true; // Strict equality

// 3. Logical Operators:

let isSunny = true;
let isCold = false;
let isNiceDay = isSunny && isCold; // Logical AND
let isWeekend = false;
let isWorkday = !isWeekend;       // Logical NOT
let isAfternoon = true;
let isRelaxing = isNiceDay || isAfternoon; // Logical OR



